def soft_delete_attempt(
    paper_attempt_id: int,
    deleted_by: int,
    conn: Connection,
) -> None:
    """Soft delete paper attempt (hide from UI, preserve audit trail).

    Sets deleted_at timestamp. Data preserved for:
    - Cost tracking (LLM spend)
    - Analytics (improvement tracking)
    - Audit trail (compliance)
    - Admin review

    Args:
        paper_attempt_id: Paper attempt ID to delete
        deleted_by: Student ID who deleted it
        conn: Database connection

    Raises:
        ValueError: If attempt doesn't exist or already deleted
    """
    # Check if exists and not already deleted
    cursor = conn.execute(
        f"SELECT id FROM {Tables.PAPER_ATTEMPTS} " f"WHERE id = ? AND deleted_at IS NULL",
        (paper_attempt_id,),
    )
    if cursor.fetchone() is None:
        raise ValueError(f"Paper attempt {paper_attempt_id} not found or already deleted")

    # Soft delete
    conn.execute(
        f"""
        UPDATE {Tables.PAPER_ATTEMPTS}
        SET deleted_at = CURRENT_TIMESTAMP,
            deleted_by = ?
        WHERE id = ?
    """,
        (deleted_by, paper_attempt_id),
    )


def restore_attempt(
    paper_attempt_id: int,
    restored_by: int,
    conn: Connection,
) -> None:
    """Restore soft-deleted paper attempt.

    Clears deleted_at timestamp to make attempt visible again.
    Used for undo functionality after accidental delete.

    Args:
        paper_attempt_id: Paper attempt ID to restore
        restored_by: Student ID who is restoring it
        conn: Database connection

    Raises:
        ValueError: If attempt doesn't exist, not deleted, or not owned by student
    """
    # Check if exists and is deleted, and owned by the student
    cursor = conn.execute(
        f"""
        SELECT id, student_id FROM {Tables.PAPER_ATTEMPTS}
        WHERE id = ? AND deleted_at IS NOT NULL
        """,
        (paper_attempt_id,),
    )
    row = cursor.fetchone()
    if row is None:
        raise ValueError(f"Paper attempt {paper_attempt_id} not found or not deleted")

    if row[1] != restored_by:
        raise ValueError(f"Paper attempt {paper_attempt_id} not owned by student {restored_by}")

    # Restore (clear deleted_at and deleted_by)
    conn.execute(
        f"""
        UPDATE {Tables.PAPER_ATTEMPTS}
        SET deleted_at = NULL,
            deleted_by = NULL
        WHERE id = ?
        """,
        (paper_attempt_id,),
    )
